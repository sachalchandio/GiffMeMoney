"""Deterministic, seeded market simulator for GiffMeMoney.

This module turns the static :mod:`app.market.universe` seeds into reproducible
price histories, OHLCV candles, and the latent factor series that the quant
models regress against. Everything is derived from a stable hash of the symbol
(and a fixed constant for the *shared* factors), so the same symbol yields the
exact same history on every run, in every process — a hard requirement for
testable CAPM / Fama-French regressions.

Model
-----
A single, universe-wide :class:`MarketModel` generates four daily series that are
**shared by every asset**:

* ``mkt`` — the market factor's *total* daily return,
* ``smb`` — the small-minus-big (size) factor daily return,
* ``hml`` — the high-minus-low (value) factor daily return,
* ``rf``  — the daily risk-free rate (constant, from settings).

Each asset's daily return is then built from a 3-factor / single-index model::

    r_t = rf_d + market_beta * (mkt_t - rf_d)
              + smb_loading * SMB_t
              + hml_loading * HML_t
              + idiosyncratic noise

The market / SMB / HML / rf processes are seeded from a *fixed* constant
(``_MARKET_SEED``) so the factor history is identical for all assets — only the
idiosyncratic component is seeded per-symbol. Prices integrate the returns from
``base_price``. Intraday OHLC is synthesised around each close with small,
seeded noise.

All public functions are numerically defensive: degenerate parameters never
produce NaN/inf prices, and prices are floored to a tiny positive value.
"""

